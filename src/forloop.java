
class example {

    public static void main(String[] args) {


                System.out.println("Printing numbers from 1 to 5:");
                for (int i = 1; i <= 5; i++) {
                    System.out.println(i);
                }

                System.out.println("Printing even numbers from 2 to 10:");
                for (int i = 2; i <= 10; i += 2) {
                    System.out.println(i);
                }


                System.out.println("Printing numbers from 10 to 1 in reverse order:");
                for (int i = 10; i >= 1; i--) {
                    System.out.println(i);
                }
            }
        }
